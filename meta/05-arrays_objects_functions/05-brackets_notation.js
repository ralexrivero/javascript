#!/usr/bin/env node

/* brackets notation
    is used to access object properties
    and used to access object methods
    syntax: object['property']
*/

const house = {};

house['color'] = 'blue';
house['size'] = 1000;
house['floors'] = 2;
house['rooms'] = 5;

console.log(house);

const house2 = {};

house2['color'] = 'red';
house2['size'] = 700;
house2['floors'] = 1;
house2['rooms'] = 4;

console.log(house2);
